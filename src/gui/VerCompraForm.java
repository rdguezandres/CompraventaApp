    private void enviarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if (compra.getEnviado()) {
            JOptionPane.showMessageDialog(this, "El producto ya ha sido enviado");
        } else {
            String selected = enviarTextField.getText();
            if (selected != null) {
                enviarTextField.setText("");
                compra.setEntrega(new Entrega(this.compra, selected));
                updateEnviado();
            } else {
                JOptionPane.showMessageDialog(null, "No se ha especificado una dirección de envío");
            }
        }

    }

    private void updateEnviado() {
        if (compra.getEnviado()) {
            enviadoLabel.setText("Enviado: Si");
        } else {
            enviadoLabel.setText("Enviado: No");
        }
    }

