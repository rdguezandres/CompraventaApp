    private void chatButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        compra.getProducto().setPrecioNegociado(true);
        ChatForm chat = new ChatForm(compra.getChat());
        chat.setVisible(true);
    }

    private void valorarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String estrellas = estrellasTextField.getText();
        if (estrellas != null) {
            try {
                if (parseInt(estrellas) > 5 || parseInt(estrellas) < 0) {
                    JOptionPane.showMessageDialog(null, "El numero de estrellas debe estar entre 1 y 5");
                } else {
                    compra.getComprador().addValoracion(new Valoracion(parseInt(estrellas), compra.getComprador(), compra.getProducto()));
                    JOptionPane.showMessageDialog(null, "Valoracion realizada");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "El numero de estrellas debe ser un numero entre 1 y 5");
            }

        } else {
            JOptionPane.showMessageDialog(null, "El numero de estrellas debe estar entre 1 y 5");
        }

    }

