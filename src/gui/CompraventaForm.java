    private void detallesButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String selected = productosList.getSelectedValue();
        if(selected != null){
            String producto = selected.split(" - ")[0];
            for(Producto p : this.model.getProductos()){
                if(p.getNombre().equals(producto)){
                    ProductoForm pf = new ProductoForm(p);
                    pf.setVisible(true);
                }
            }
        }
    }

    private void comprarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String selected = productosList.getSelectedValue();
        if(selected != null){
            String producto = selected.split(" - ")[0];
            for(Producto p : this.model.getProductos()){
                if(p.getNombre().equals(producto)){
                    ComprarForm comprarForm = new ComprarForm(this.model, p);
                    comprarForm.setVisible(true);
                    this.setVisible(false);
                }
            }

        }
    }

    private void vercompraButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String selected = comprasList.getSelectedValue();
        if(selected != null){
            String compra = selected.split(" - ")[0];
            for(Compra c : this.model.getCompras()){
                if(c.getProducto().getNombre().equals(compra)){
                    VerCompraForm vcf = new VerCompraForm(c);
                    vcf.setVisible(true);
                }
            }
        }
    }

    private void favoritosButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String selected = productosList.getSelectedValue();
        if (selected != null) {
            String producto = selected.split(" - ")[0];
            for(Producto p : this.model.getProductos()){
                if(p.getNombre().equals(producto)){
                    p.setFavorito(!p.isFavorito());
                }
            }
        }
        updateFavoritos();
    }

    private void updateFavoritos() {
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Producto p : this.model.getProductos()) {
            if (p.isFavorito()) {
                model.addElement(p.getNombre());
            }
        }
        favoritosList.setModel(model);
    }

    public void updateCompras(){
        jScrollPane4.setViewportView(comprasList);

        String[] compras = new String[model.getCompras().size()];
        for(int i = 0; i < compras.length; i++){
            compras[i] = model.getCompras().get(i).toString();
        }
        updateProductos();
        updateFavoritos();
        comprasList.setListData(compras);
    }

    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        updateProductos();
    }

    public void updateProductos(){
        String texto = buscador.getText();
        ArrayList<String> lista = new ArrayList<String>();
        for(Producto p : this.model.getProductos()){
            if(p.getNombre().toLowerCase().contains(texto.toLowerCase())){
                lista.add(p.getNombre() + " - " + p.getPrecio() + "â‚¬");
            }
            String[] resultado = new String[lista.size()];
            for(int i = 0; i < lista.size(); i++){
                resultado[i] = lista.get(i);
            }
            productosList.setListData(resultado);
        }
    }

